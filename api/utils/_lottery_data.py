def get_latest_result():
    """获取最新一期开奖结果"""
    if LOTTERY_HISTORY:
        data = LOTTERY_HISTORY[0]
        return {
            'period': data[0],
            'front_zone': list(data[1:6]),
            'back_zone': list(data[6:8]),
            'draw_date': data[8]
        }
    return None


def get_recent_results(n=10):
    """获取最近n期开奖结果"""
    results = []
    for data in LOTTERY_HISTORY[:n]:
        results.append({
            'period': data[0],
            'front_zone': list(data[1:6]),
            'back_zone': list(data[6:8]),
            'draw_date': data[8]
        })
    return results


